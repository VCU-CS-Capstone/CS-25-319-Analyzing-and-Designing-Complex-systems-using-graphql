SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS CUSTOMERS;
DROP TABLE IF EXISTS TRANSACTIONS;
DROP TABLE IF EXISTS INVENTORY;
DROP TABLE IF EXISTS ITEMSPURCHASED;
SET FOREIGN_KEY_CHECKS = 1;

CREATE TABLE IF NOT EXISTS CUSTOMERS (
    customerId INT not NULL,
    firstName VARCHAR(255),
    lastName VARCHAR(255),
    'address' VARCHAR(255),
    lastPurchaseDate VARCHAR(255),
    email VARCHAR(255),
    phone VARCHAR(255),
    PRIMARY KEY(customerId)
);


CREATE TABLE IF NOT EXISTS TRANSACTIONS(
    transactionId INT not NULL,
    transactionDate VARCHAR(255),
    buyerId INT not null,
    sellerId int not null,
    quantity int,
    totalPrice INT,
    PRIMARY KEY(transactionId)
);


CREATE TABLE IF NOT EXISTS INVENTORY (
    itemId int not NULL,
    availableQuantity int,
    itemDescription VARCAHR(255),
    itemPrice int not null,
    PRIMARY KEY (itemId)
);

CREATE TABLE IF NOT EXISTS ITEMSPURCHASED (
    purchaseId,
    itemId,
    transactionId,
    PRIMARY KEY (purchaseId)
);

alter table TRANSACTIONS add FOREIGN key (buyerId) REFERENCES CUSTOMERS(customerId);
alter table TRANSACTIONS add FOREIGN key (sellerId) REFERENCES CUSTOMERS(customerId);
alter table ITEMSPURCHASED add FOREIGN key (itemId) REFERENCES INVENTORY(itemId);
alter table ITEMSPURCHASED add FOREIGN key (transactionId) REFERENCES TRANSACTIONS(transactionId);


